#Напишіть програму, яка друкує в дному рядку через пропуск усі парні числа від 1 до n (1 < n ≤ 100, n - ціле число, 
# яке вводить користувач). Використайте оператор continue.
#Вхідні дані:2, 10, 7

n=int(input("введіть число від 1 до 100:\n"))
for i in range(1, n+1):
    if i%2!=0:
        continue
    print(i,end=" ",)
