#Надрукувати всі непарні двоцифрові числа, у яких остання цифра дорівнює n - ціле число, яке вводить користувач.
#Вхідні дані:5, 1, 0
n=int(input("введіть число:\n"))
last_n=[]
for i in range(10,100):  #задаю range двоцифрові числа
    if i%2!=0 and i%10==n: #вибірка на непарні цифри  і що залтшок від ділення було n-закінчення.
     last_n.append(i)     
print(last_n)