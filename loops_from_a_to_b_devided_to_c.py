#Надрукувати всі цілі числа від a до b включно, 
# кратні деякому числу c. Числа a, b, c - цілі числа, які вводить користувач.
#Вхідні дані:20, 50, 3
a=int(input('введіть число a:\n')) #від якого числа
b=int(input('введіть число b:\n')) #до якого числа
c=int(input('введіть число c:\n')) #число на яке кратне
ab_list=[]
for i in range(a, b+1):
    if i%c==0:
     ab_list.append(i)
print(ab_list)    